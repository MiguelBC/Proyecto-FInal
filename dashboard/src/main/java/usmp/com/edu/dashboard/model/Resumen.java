package usmp.com.edu.dashboard.model;

public class Resumen {

    private int cantidadviajes;
    private int cantidadcorreos;
    private int cantidadCalculo;
    private int cantidadPerson;


    public int getCantidadviajes() {
        return cantidadviajes;
    }

    public void setCantidadviajes(int cantidadviajes) {
        this.cantidadviajes = cantidadviajes;
    }

    public int getCantidadcorreos() {
        return cantidadcorreos;
    }

    public void setCantidadcorreos(int cantidadcorreos) {
        this.cantidadcorreos = cantidadcorreos;
    }

    public int getCantidadCalculo() {
        return cantidadCalculo;
    }

    public void setCantidadCalculo(int cantidadCalculo) {
        this.cantidadCalculo = cantidadCalculo;
    }

    public int getCantidadPerson() {
        return cantidadPerson;
    }

    public void setCantidadPerson(int cantidadPerson) {
        this.cantidadPerson = cantidadPerson;
    }
}
